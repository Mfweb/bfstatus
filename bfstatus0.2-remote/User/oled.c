void oled_init(void)
{
	uint8_t buf[21]={
	0xae,//0xae:关显示，0xaf:开显示
	0xd5,0x80,//显示时钟频率？
	0xa8,0x3f,//复用率？
	0xd3,0x00,//显示偏移？
	0x40,//显示开始线
	0x8d,0x14,//VCC电源
	0xa1,//设置段重新映射？
	0xc8,//COM输出方式？
	0xda,0x12,//COM输出方式？
	0x81,0xff,//对比度，指令：0x81，数据：0~255（255最高）
	//0xd9,0xf1,//充电周期？
	//0xdb,0x30,//VCC电压输出
	0x20,0x00,//水平寻址设置
	0xa4,//0xa4:正常显示，0xa5:整体点亮
	0xa6,//0xa6:正常显示，0xa7:反色显示
	0xaf//0xae:关显示，0xaf:开显示
	}; //

	GPIO_QuickInit(GPIOB, GPIO_Pin_2, GPIO_Mode_Out_PP);
	GPIO_WriteBit(GPIOB, GPIO_Pin_2, Bit_RESET);
	DelayMs(50);
	GPIO_WriteBit(GPIOB, GPIO_Pin_2, Bit_SET);
	DelayMs(50);
	//LED1 = i2c_CheckDevice(OLED_ADDRESS);
	
	write_bytes(OLED_ADDRESS,0x00,buf,21);

	oled_clear();
}

void oled_show_bit(uint8_t bit)
{
	write_byte(OLED_ADDRESS,0x40,bit);
}

void oled_write_char(char c)
{
  unsigned char line;	
  c -= 32;
  for (line=0; line<6; line++)
     oled_show_bit(font6x8[c][line]);
}

void oled_set_xy(uint8_t x,uint8_t y)
{
	write_byte(OLED_ADDRESS,0x00,0xb7 - (7 - y));
	if (x%2)
		write_byte(OLED_ADDRESS,0x00,0x08);
	else
		write_byte(OLED_ADDRESS,0x00,0x00);
	write_byte(OLED_ADDRESS,0x00,0x10+(x>>1));
	
}
//写入英文字符串 （必须保证有英文字库）
void oled_write_english_string(uint8_t X,uint8_t Y,char *s)
{
 oled_set_xy(X,Y);
  while (*s) 
  {
		oled_write_char(*s);
		s++;
   }
}
void oled_printf(uint8_t x,uint8_t y,const char *fmt, ...)
{
	char outbuf[128/6];
	va_list ap;
	va_start(ap,fmt);
	vsprintf(outbuf,fmt,ap);
	oled_write_english_string(x,y,outbuf);
	va_end(ap);
}

void oled_clear(void)
{
	uint16_t i;
	for (i=0 ;i<1024;i++)
		write_byte(OLED_ADDRESS,0x40,0);
}

